def average_marks(*args):
    valid_marks=[mark for mark in args if mark>=0]
    if len(valid_marks) ==0:
     return 0
    
    return sum (valid_marks)/len(valid_marks)
print(average_marks(10,20,-5,30))
print(average_marks(-6,-9,30))

